    private void btnRegistroRegActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistroRegActionPerformed

        try{
           
            //TOMA DE DATOS
            String nombreNew=txtnomReg.getText();
            String usuarioNew=txtusuReg.getText();
            String passNew=String.valueOf(txtpassReg.getPassword());
            
            //LECTURA
            File archivo = new File("registros.txt");
            FileReader fr = new FileReader(archivo);
            BufferedReader br = new BufferedReader(fr);
            
            String linea;
            
            while((linea=br.readLine())!=null){
                String nomusuario=br.readLine();
                String pass=br.readLine();
                
                Usuario usuario = new Usuario(nomusuario,pass,linea);
                listaUsuarios.add(usuario);
            }
            
            Boolean estado=false; //Estado (false) = no registrado | (true) = registrado
            
            //VALIDACION DE CAMPOS RELLENADOS
            if(nombreNew.equals("") || usuarioNew.equals("") || passNew.equals("")){
                errorUsu.setText("Rellena los campos");
                estado=true;
            }
            
            //VERIFICACION DE EXISTENCIA DE USUARIO
            for(int i=0;i<listaUsuarios.size();i++){
                if(listaUsuarios.get(i).getNombreUsuario().equals(usuarioNew)){
                    errorUsu.setText("Usuario Existente");
                    estado=true;
                    break;
                }
            }
            
            //ESCRITURA DE USUARIO
            if(estado==false){
                    PrintWriter writer = new PrintWriter(new BufferedWriter(new FileWriter("registros.txt", true)));
            
                    writer.println(nombreNew);
                    writer.println(usuarioNew);
                    writer.println(passNew);
                    writer.close();
            
                    JOptionPane.showMessageDialog(null, "USUARIO REGISTRADO CON EXITO!!!");
            
                    this.dispose();
                    LoginReg login=new LoginReg();
                    login.setVisible(true);
                    
            }
            
            
            
        }catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnRegistroRegActionPerformed

